import invoiceModel from '../models/invoiceModel.js';

class InvoiceController {
    async getInvoices(req, res) {
        try {
            if (req.user.role === 'tenant') {
                const invoices = await invoiceModel.findByTenantId(req.user.id);
                return res.json(invoices);
            } else if (req.user.role === 'owner' || req.user.role === 'treasurer') {
                const invoices = await invoiceModel.findAll();
                return res.json(invoices);
            } else {
                return res.status(403).json({ error: 'Access denied' });
            }
        } catch (error) {
            console.error(error);
            res.status(500).json({ error: 'Failed to fetch invoices' });
        }
    }

    // Manual generation for testing? 
    // Usually invoices are generated by a cron job or Lease logic.
    // Let's add a manual create for Owner to seed data.
    async createInvoice(req, res) {
        try {
            if (req.user.role !== 'owner') return res.status(403).json({ error: 'Denied' });

            const { leaseId, tenantId, propertyId, amount, dueDate, description } = req.body;
            const id = await invoiceModel.create({
                leaseId, tenantId, propertyId, amount, dueDate, description
            });
            res.status(201).json({ message: 'Invoice created', id });
        } catch (error) {
            console.error(error);
            res.status(500).json({ error: 'Error creating invoice' });
        }
    }
}

export default new InvoiceController();
